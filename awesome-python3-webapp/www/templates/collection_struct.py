def bmp_info(data):
    d=struct.unpack('<ccIIIIIIHH', data[:30])#unpack限制必须为30位缓存数据
    if bytes.decode(d[0]+d[1]) == 'BM':#判定是否为Windows下BMP位图，OS位图为‘BA’
        return {
            'width': d[6],
            'height': d[7],
            'color': d[9]
        }
    else:
        return '文件类型不正确'
